# Loading bar
loading_bar() {
    local steps=20
    for ((i=0; i<=steps; i++)); do
        percent=$((i * 100 / steps))
        filled=$((i * 20 / steps))
        empty=$((20 - filled))
        
        printf "\r["
        for ((j=0; j<filled; j++)); do
            printf "#"
        done
        for ((j=0; j<empty; j++)); do
            printf "-"
        done
        printf "] %3d%%" "$percent"
        sleep 0.15
    done
    echo
}

loading_bar

